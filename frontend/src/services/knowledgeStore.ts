function mergeById(base: KnowledgeArticle[], extra: KnowledgeArticle[]) {
  const map = new Map<string, KnowledgeArticle>()
  for (const a of base) map.set(a.id, a)
  for (const a of extra) map.set(a.id, a)
  return Array.from(map.values())
}

export function readArticles(): KnowledgeArticle[] {
  try {
    // v2 优先
    const rawV2 = window.localStorage.getItem(STORAGE_KEY_V2)
    if (rawV2) {
      const parsed = JSON.parse(rawV2)
      if (Array.isArray(parsed)) return mergeById(seed, parsed as KnowledgeArticle[])
    }

    // 兼容旧 v1：读取后与 seed 合并，并写入 v2
    const rawV1 = window.localStorage.getItem(STORAGE_KEY_V1)
    if (!rawV1) return seed
    const parsedV1 = JSON.parse(rawV1)
    if (!Array.isArray(parsedV1)) return seed
    const merged = mergeById(seed, parsedV1 as KnowledgeArticle[])
    window.localStorage.setItem(STORAGE_KEY_V2, JSON.stringify(merged))
    return merged
  } catch {
    return seed
  }
}

export function writeArticles(items: KnowledgeArticle[]) {
  window.localStorage.setItem(STORAGE_KEY_V2, JSON.stringify(items))
  window.dispatchEvent(new Event('knowledge:changed'))
}
